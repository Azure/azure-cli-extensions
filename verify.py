"""
Verify an SSH certificate generated by `az ssh cert-create`.

Usage:
    python verify.py --cert <ssh-cert.pub> --metadata <metadata.json> --ca-pub <ca_public.pem>

Where:
    --cert       Path to the certificate file (ssh-cert.pub)
    --metadata   Path to a JSON file with the signing payload metadata
    --ca-pub     Path to the CA public key (PEM). Download with:
                 az keyvault key download --vault-name <vault> --name ssh-ca --encoding PEM -f ca_public.pem
"""

import argparse
import json
import hashlib
import base64
import sys

from cryptography.hazmat.primitives.asymmetric import padding, utils
from cryptography.hazmat.primitives import hashes, serialization
from cryptography.exceptions import InvalidSignature


def verify_certificate(cert_path, metadata_path, ca_pub_path):
    # 1. Load CA public key
    with open(ca_pub_path, "rb") as f:
        ca_pub = serialization.load_pem_public_key(f.read())
    print(f"[OK] Loaded CA public key from {ca_pub_path}")

    # 2. Load metadata (the signing payload)
    with open(metadata_path, "r", encoding="utf-8") as f:
        metadata = json.load(f)
    print(f"[OK] Loaded metadata: {json.dumps(metadata, indent=2)}")

    # 3. Read certificate file — current format: "<publicKey> <signature_b64>"
    with open(cert_path, "r", encoding="utf-8") as f:
        cert_content = f.read().strip()

    # Extract signature (last space-separated token)
    parts = cert_content.rsplit(" ", 1)
    if len(parts) != 2:
        print("[FAIL] Certificate format unexpected — expected '<publicKey> <signature>'")
        return False
    cert_public_key_part, signature_b64 = parts[0], parts[1]
    print(f"[OK] Extracted signature from certificate ({len(signature_b64)} chars)")

    # 4. Verify public key in cert matches metadata
    if metadata.get("userPublicKey", "").strip() != cert_public_key_part.strip():
        print("[FAIL] Public key in certificate does not match metadata userPublicKey")
        return False
    print("[OK] Public key in certificate matches metadata")

    # 5. Reconstruct the digest that was signed
    canonical = json.dumps(
        metadata, separators=(",", ":"), sort_keys=True, ensure_ascii=True
    ).encode("utf-8")
    digest = hashlib.sha256(canonical).digest()
    print(f"[OK] Computed SHA-256 digest of canonical metadata")

    # 6. Decode the signature (base64url)
    # Add padding if needed
    sig_padded = signature_b64 + "=" * (4 - len(signature_b64) % 4)
    sig_bytes = base64.urlsafe_b64decode(sig_padded)
    print(f"[OK] Decoded signature ({len(sig_bytes)} bytes)")

    # 7. Verify: CA public key + RS256 (PKCS1v15 + SHA256 prehashed)
    try:
        ca_pub.verify(
            sig_bytes,
            digest,
            padding.PKCS1v15(),
            utils.Prehashed(hashes.SHA256()),
        )
        print("[OK] Signature is VALID — certificate was signed by the CA key")
        return True
    except InvalidSignature:
        print("[FAIL] Signature is INVALID — certificate was NOT signed by this CA key")
        return False


def main():
    parser = argparse.ArgumentParser(description="Verify an az ssh cert-create certificate")
    parser.add_argument("--cert", required=True, help="Path to ssh-cert.pub")
    parser.add_argument("--metadata", required=True, help="Path to metadata JSON file")
    parser.add_argument("--ca-pub", required=True, help="Path to CA public key (PEM)")
    args = parser.parse_args()

    ok = verify_certificate(args.cert, args.metadata, args.ca_pub)
    sys.exit(0 if ok else 1)


if __name__ == "__main__":
    main()