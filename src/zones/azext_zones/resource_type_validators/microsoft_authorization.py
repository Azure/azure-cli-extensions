from .._resourceTypeValidation import (
    ZoneRedundancyValidationResult,
    register_resource_type,
)
from knack.log import get_logger


# pylint: disable=too-few-public-methods
@register_resource_type("microsoft.authorization")
class microsoft_authorization:
    @staticmethod
    def validate(resource):
        resourceType = resource["type"]
        resourceSubType = resourceType[resourceType.index("/") + 1:]

        _logger = get_logger("microsoft_authorization")
        _logger.debug(
            "Validating Microsoft.authorization resource type: %s", resourceSubType
        )

        # authorization resources are always zone redundant
        return ZoneRedundancyValidationResult.Always
